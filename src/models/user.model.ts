import { Schema, model } from 'mongoose';
import IUser from '../types/users.type';

const userSchema = new Schema<IUser>(
  {
    email: {
      type: String,
      required: true,
      unique: true,
    },
    name: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true },
);

export const userModel = model<IUser>('User', userSchema);
